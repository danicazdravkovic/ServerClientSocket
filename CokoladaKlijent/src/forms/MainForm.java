    private void srediFormu() {
        popuniKupce();
        txtUkupanIznos.setText("0.00");
        txtUkupanIznos.setEnabled(false);
        txtCenaStavke.setText("0.00");
        txtCenaStavke.setEnabled(false);
        popuniVrsteCokolade();
        tblStavke.setModel(new TableModelStavkeRacuna());
    }

    public void popuniKupce() {
        try {
            ArrayList<Kupac> kupci = ClientController.getInstance().getAllKupac();

            cmbKupac.removeAllItems();

            for (Kupac kupac : kupci) {
                cmbKupac.addItem(kupac);
            }

        } catch (Exception ex) {
            Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void popuniVrsteCokolade() {
        try {
            ArrayList<VrstaCokolade> vrsteCokolada = ClientController.getInstance().getAllVrstaCokolade();

            cmbVrstaCokolade.removeAllItems();

            for (VrstaCokolade vrstaCokolade : vrsteCokolada) {
                cmbVrstaCokolade.addItem(vrstaCokolade);
            }

            popuniCokolade(vrsteCokolada.get(0));

        } catch (Exception ex) {
            Logger.getLogger(FormNovaCokolada.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void popuniCokolade(VrstaCokolade vc) {
        try {
            ArrayList<Cokolada> cokolade = ClientController.getInstance().getAllCokolada(vc);

            cmbCokolada.removeAllItems();

            for (Cokolada cokolada : cokolade) {
                cmbCokolada.addItem(cokolada);
            }

        } catch (Exception ex) {
            Logger.getLogger(FormNovaCokolada.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

